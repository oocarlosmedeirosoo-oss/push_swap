#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*new;
	size_t	i;
	size_t	j;

	if (!s1 || !set)
		return (NULL);
	i = 0;
	while (s1[i] && ft_strchr(set, s1[i]))
		i++;
	j = ft_strlen(s1);
	while (j > i && ft_strchr(set, s1[j - 1]))
		j--;
	new = malloc(sizeof(char) * (j - i + 1));
	if (!new)
		return (NULL);
	ft_strlcpy(new, &s1[i], j - i + 1);
	return (new);
}
